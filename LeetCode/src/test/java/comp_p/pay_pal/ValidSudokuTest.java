package comp_p.pay_pal;

import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

class ValidSudokuTest {

    @Test
    void testIsValidSudokuTrue() {
        char[][] board ={
                {'5','3','.','.','7','.','.','.','.'},
                {'6','.','.','1','9','5','.','.','.'},
                {'.','9','8','.','.','.','.','6','.'},
                {'8','.','.','.','6','.','.','.','3'},
                {'4','.','.','8','.','3','.','.','1'},
                {'7','.','.','.','2','.','.','.','6'},
                {'.','6','.','.','.','.','2','8','.'},
                {'.','.','.','4','1','9','.','.','5'},
                {'.','.','.','.','8','.','.','7','9'}};
        assertTrue(ValidSudoku.isValidSudoku(board));
    }

    @Test
    void testIsValidSudokuFalse() {
        char[][] board ={
                {'.','.','.','.','5','.','.','1','.'},
                {'.','4','.','3','.','.','.','.','.'},
                {'.','.','.','.','.','3','.','.','1'},
                {'8','.','.','.','.','.','.','2','.'},
                {'.','.','2','.','7','.','.','.','.'},
                {'.','1','5','.','.','.','.','.','.'},
                {'.','.','.','.','.','2','.','.','.'},
                {'.','2','.','9','.','.','.','.','.'},
                {'.','.','4','.','.','.','.','.','.'}};
        assertFalse(ValidSudoku.isValidSudoku(board));

        char[][] board2 = {
                {'9','.','.','6','.','.','.','.','.'},
                {'.','.','.','.','6','.','.','.','.'},
                {'.','.','.','.','.','1','.','3','.'},
                {'.','.','.','.','.','.','.','.','8'},
                {'.','.','.','.','.','8','.','.','.'},
                {'.','.','.','4','.','.','2','.','.'},
                {'.','.','.','.','.','.','.','.','1'},
                {'6','.','.','.','1','.','.','.','.'},
                {'.','.','.','.','.','.','.','.','.'}};
            assertFalse(ValidSudoku.isValidSudoku(board2));
    }
}