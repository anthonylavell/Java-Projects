package utility;

public class Number {
    public static void main(String[] args) {
        displayNum();
        displayNum2();
    }

    public static void displayNum(){
        System.out.println(1);
        System.out.println(2);
        System.out.println(3);
    }

    public static void displayNum2(){
        for (int i = 1; i <4; i++){
            System.out.println(i);
        }
    }
}
